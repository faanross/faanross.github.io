	<h2>Jevons Paradox</h2>

	<p>
		In 1865, economist William Stanley Jevons observed something counterintuitive about coal consumption in England. As steam engines became more efficient - requiring less coal per unit of work - total coal consumption didn't decrease. It increased. Dramatically.
	</p>

	<p>
		Why? Because efficiency made coal-powered machines economically viable for more applications. The cheaper something becomes to use, the more you use it.
	</p>

	<hr />

	<h2>The Trap</h2>

	<figure class="article-image">
		<img src="/images/claude/jevons-paradox/IMG-002-THE-TRAP.png" alt="The productivity trap" />
	</figure>

	<p>
		Here's what happened: Claude Code didn't just make my existing work faster. It expanded the horizon of what felt possible.
	</p>

	<p>
		Before, I had a mental filter. Some projects were too ambitious, too time-consuming, not worth pursuing given other priorities. That filter kept my commitments bounded.
	</p>

	<p>
		But when suddenly you <em>can</em> build that side project, <em>can</em> create that course, <em>can</em> maintain that additional workflow - when the cost of execution drops by an order of magnitude - the filter breaks. Your sense of what's "within reach" expands faster than the time you're saving.
	</p>

	<p>New repos get created. New content pipelines emerge. The todo list grows.</p>

	<p>I became more productive per hour. So I filled more hours with production.</p>

	<hr />

	<h2>The Uncomfortable Truth</h2>

	<p>
		I think many of us harbor a fantasy about productivity tools. The fantasy goes: once I find the right system, the right workflow, the right AI assistant - I'll finally have space. I'll work less. I'll be more relaxed. Life will open up.
	</p>

	<p>But here's the truth: that spaciousness will never come from a tool. No matter how powerful.</p>

	<p>
		A tool that helps you produce more will, by default, help you produce more. That's it. That's what it does. It doesn't automatically give you a lighter schedule or a calmer mind. It gives you capacity - and what you do with that capacity is entirely your choice.
	</p>

	<p>If you don't consciously decide to work less, you might just work more. Because now you can.</p>

	<hr />

	<h2>The Real Decision</h2>

	<p>
		The shape of your life has to be a deliberate choice - it'll never miraculously manifest as an emergent property of better tooling.
	</p>

	<p>
		I'm (clearly) still figuring this out. The goal has never been maximum output. The goal is a good life that includes meaningful work. Those aren't the same thing.
	</p>

